-- 개념정리

-- 1. 정형데이터란?
-- >> 저장할 데이터(항목)가 정해져있는 데이터

-- 2. DBMS란?
-- >> DB를 관리해주는 프로그램

-- 3. 당신이 알고있는 DBMS의 종류 2가지 이상?
-- >> 오라클, mySQL ...

-- 4. SQL은 무엇의 약자인가?
-- >> Structured Query Language (체계적으로 정리된 명령어의 모음)

-- 5. SQL의 종류 3가지 분류
-- >> DDL, DML, DCL

-- 6. CRUD는 무엇의 약자인가?
-- >> Create, Read, Update, DBeaver

-- 7. 당신이 알고있는 DB클라이언트 프로그램은?
-- >> Dbeaver

-- 8. DML중 데이터를 넣을 때 사용하는 구문은?
-- >> insert

-- 9. DML중 데이터를 검색할 때 사용하는 구문은?
-- >> select